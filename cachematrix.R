## This is my function to cache inverse of a matrix, just following the given example.

## makeCacheMatrix is a list containg functions
makeCacheMatrix <- function(x =matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinverse <- function(inverse) m <<- inverse
        getinverse <- function() m
        list(set = set, get = get,
             setinverse = setinverse,
             getinverse = getinverse)
}


